package punic.iterator;

import punic.core.Warrior;

/**
 * Интерфейс итератора для обхода коллекции воинов.
 * Паттерн Iterator предоставляет механизм обхода элементов составных объектов
 * (коллекций) не раскрывая их внутреннего представления.
 */
public interface WarriorIterator {
    /**
     * Проверяет, есть ли следующий элемент в коллекции.
     * @return true, если есть следующий элемент, иначе false
     */
    boolean hasNext();

    /**
     * Возвращает следующий элемент коллекции.
     * @return следующий воин в коллекции
     */
    Warrior next();

    /**
     * Сбрасывает итератор в начало коллекции.
     */
    void reset();
}

